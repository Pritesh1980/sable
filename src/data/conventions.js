export function getConventionFavicon(convention) {
  if (!convention.url) return ''
  try {
    const { hostname } = new URL(convention.url)
    return `https://www.google.com/s2/favicons?domain=${hostname}&sz=128`
  } catch {
    return ''
  }
}
